import React from 'react';
import { Helmet } from 'react-helmet-async';

interface SEOProps {
  title?: string;
  description?: string;
  keywords?: string;
  image?: string;
  url?: string;
  type?: string;
}

const SEO: React.FC<SEOProps> = ({
  title = 'CodeKnox - The Stronghold Of Smart Solutions',
  description = "CodeKnox is Sri Lanka's premier web development company specializing in professional websites, mobile apps, and digital transformation.",
  keywords = 'codeknox, web development Sri Lanka, website design Colombo, mobile app development, software development',
  image = 'https://codeknox.lk/logo.png',
  url = 'https://codeknox.lk/',
  type = 'website'
}) => {
  const siteTitle = title.includes('CodeKnox') ? title : `${title} | CodeKnox`;

  return (
    <Helmet>
      {/* Standard Metadata */}
      <title>{siteTitle}</title>
      <meta name="description" content={description} />
      <meta name="keywords" content={keywords} />
      <link rel="canonical" href={url} />

      {/* Open Graph / Facebook */}
      <meta property="og:type" content={type} />
      <meta property="og:url" content={url} />
      <meta property="og:title" content={siteTitle} />
      <meta property="og:description" content={description} />
      <meta property="og:image" content={image} />

      {/* Twitter */}
      <meta name="twitter:card" content="summary_large_image" />
      <meta name="twitter:url" content={url} />
      <meta name="twitter:title" content={siteTitle} />
      <meta name="twitter:description" content={description} />
      <meta name="twitter:image" content={image} />
    </Helmet>
  );
};

export default SEO;
